package ex_01_Java_Basics;

public class lab004_Common_Math {
    public static void main(String[] args) {
        System.out.println("2+2"); //When Using Double Quotes It Becomes Multi Character String It Comes As it is
        System.out.println(2+2);
        System.out.println(2*2);
        System.out.println(2/2);
        System.out.println(2-2);
        System.out.println(3/2); // Println Will Always Use Integer Parts No Decimal
    }
}
