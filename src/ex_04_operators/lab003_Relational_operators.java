package ex_04_operators;

public class lab003_Relational_operators {
    public static void main(String[] args) {
        int a = 20;
        int b = 30;
        boolean c = a > b;
        System.out.println(c);

        int age_hema = 40;
        int age_vivek = 33;
        boolean result = age_hema > age_vivek;
        System.out.println(result);
    }
}
